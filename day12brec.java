    private static int calculateArrangements(String record, String[] array) {
        String schema = construct(array);
        int springNumber = culcSpringNumber(array);
        int count = 0;

        ArrayList<String> variants = generateArrangements(record);
        ArrayList<String> allVariant = multiplicateArray(variants, springNumber);

        Pattern pattern = Pattern.compile(schema);
        Matcher matcher;

        for (String variant : allVariant) {
            matcher = pattern.matcher(variant);
            boolean b = matcher.matches();
            if (b) {
                count += 1;
            }
        }

        System.out.println("Record = " + record + " -- key = " + schema);
        System.out.print(" -- result = ");

        return count;
    }

    private static ArrayList<String> multiplicateArray(ArrayList<String> variants, int springNumber) {
        ArrayList<String> result = variants;
        for (int i = 0; i < KM; i++) {
            ArrayList<String> temp = new ArrayList<>();
            for (int j = 0; j < result.size(); j++) {
                for (int k = 0; k < variants.size(); k++) {
                    String newItem1 = result.get(j) + "#" + variants.get(k);
                    if (isNotAlotSprings(newItem1.toCharArray(), springNumber)) {
                        temp.add(newItem1);
                    }
                    String newItem2 = result.get(j) + "." + variants.get(k);
                    if (isNotAlotSprings(newItem2.toCharArray(), springNumber)) {
                        temp.add(newItem2);
                    }
                }
            }
            result = temp;
        }
        System.out.println("Size of Array = " + result.size());
        System.out.println(result);
        return result;
    }

    private static int culcSpringNumber(String[] array) {
        int number = 0;
        for (int i = 0; i < array.length; i++) {
            number += Integer.parseInt(array[i]);
        }
        return number * (1 + KM);
    }

    private static ArrayList<String> generateArrangements(String input) {
        ArrayList<String> result = new ArrayList<>();
        char[] chars = input.toCharArray();
        generateArrangementRecursion(chars, 0, result);
        return result;
    }

    private static void generateArrangementRecursion(char[] chars, int index, ArrayList<String> result) {
        if (index == chars.length) {
            result.add(new String(chars));
            return;
        }
        if (chars[index] == '?') {
            chars[index] = '#';             // Change "?" on "#" and start recursion
            generateArrangementRecursion(chars, index + 1, result);

            chars[index] = '.';             // Change "?" on "." and start recursion
            generateArrangementRecursion(chars, index + 1, result);

            chars[index] = '?';             // Return "?" for other iteration
        } else {
            // If char is not "?", start recursion for next char
            generateArrangementRecursion(chars, index + 1, result);
        }
    }

    private static boolean isNotAlotSprings(char[] chars1, int maxNumber) {
        int number = 0;
        for (char c : chars1) {
            if (c == '#') number += 1;
        }
        return number <= maxNumber;
    }

    private static String construct(String[] array) {
        String result = "^\\.*";
        for (int k = 0; k < 5; k++) {
            for (int i = 0; i < array.length; i++) {
                for (int j = 0; j < Integer.parseInt(array[i]); j++) {
                    result += "#";
                }
                if (k == KM && i == (array.length - 1)) {
                    result += "\\.*";
                } else result += "\\.+";
            }
        }
        result += "$";
        return result;
    }

